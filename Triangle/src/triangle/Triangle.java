
package triangle;


public class Triangle {


    public static void main(String[] args) {
        int i,j;
        for(i=1;i<=30;i=i+5){
            for(j=1;j<=i;j++){
                
                System.out.print("*"); //print is normal print 
            }
            System.out.println("\n"); //println is print in new line
        }
    }
    
}
