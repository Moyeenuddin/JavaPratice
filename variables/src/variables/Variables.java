/*
 * Variable declaration of diffarent data type
 */
package variables;


public class Variables {

    public static void main(String[] args) {
        
        byte mybyte=127;            //8-bit  -128 to 127
        short myshort=16;           //16-bit    -32,768 to 32,767
        int myint=16;               //32-bit   - 2,147,483,648 to 2,147,483,647
        long mylong=8909;           //64-bit    -9,223,372,036,854,775,808(-2^63)to 9,223,372,036,854,775,807 (2^63 -1)
        float myfloat=1.2f;         //32-bit    
        double mydouble=9209.398;   //64-bit
        boolean myboolian= true;    //default value is false
        char mychar='A';            //16-bit Single Unicode character
        
        
        
        
        System.out.println(mybyte);
        System.out.println(myshort);
        System.out.println(myint);
        System.out.println(mylong);
        System.out.println(myfloat);
        System.out.println(mydouble);
        System.out.println(myboolian);
        System.out.println(mychar);
        System.out.println(mydouble);
        
             
                
    }
    
}
